CREATE TABLE IF NOT EXISTS TB_TYPE_RENTITEM
(
    ID_TYPE_RENTITEM INT AUTO_INCREMENT PRIMARY KEY,
    ID_COMPANY VARCHAR(50),
    NAME_TYPE_RENTITEM VARCHAR(50) NOT NULL,
    STATE ENUM("PUBLIC","PRIVATE") DEFAULT "PUBLIC",
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_COMPANY) REFERENCES TB_COMPANY (ID_COMPANY)
);

CREATE TABLE IF NOT EXISTS TB_RENTITEM
(
    ID_RENTITEM INT AUTO_INCREMENT PRIMARY KEY,
    ID_COMPANY VARCHAR(50),
    NAME_RENTITEM VARCHAR(50) NOT NULL,
    DESCRIPTION VARCHAR(100) NOT NULL,
    PRICE DECIMAL(10,2),
    COST DECIMAL(10,2),
    IMAGE VARCHAR(50) NOT NULL,
    LINK_MORE VARCHAR(100) NOT NULL,
    STATE ENUM("PUBLIC","PRIVATE") DEFAULT "PUBLIC",
    SHOW_RENTITEM ENUM("ONLYUSER","ALL") DEFAULT "ONLYUSER",
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_COMPANY) REFERENCES TB_COMPANY (ID_COMPANY)
);

CREATE TABLE IF NOT EXISTS TB_GROUP_RESERVE_RENTITEM
(
    ID_GROUP_RESERVE_RENTITEM INT AUTO_INCREMENT PRIMARY KEY,
    ID_GROUP_RESERVE_COMPANY VARCHAR(50),
    ID_RENTITEM INT,
    DESCRIPTION VARCHAR(255) NOT NULL, 
    STATUS ENUM("PENDING", "REJECT", "APPROVE", "SUCCESS") DEFAULT "PENDING",
    STATUS_PAY ENUM("PENDING","PAY") DEFAULT "PENDING",
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_GROUP_RESERVE_COMPANY) REFERENCES TB_GROUP_RESERVE_COMPANY (ID_GROUP_RESERVE_COMPANY),
    FOREIGN KEY (ID_RENTITEM) REFERENCES TB_RENTITEM (ID_RENTITEM)
);