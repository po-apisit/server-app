CREATE TABLE IF NOT EXISTS TB_TRANSATION
(
    ID_TRANSATION INT AUTO_INCREMENT PRIMARY KEY,
    PRARENT_ID VARCHAR(50), -- bil
    STATUS_PAY ENUM("PENDING","CANCLE","PAY") DEFAULT "PENDING",
    COMMENT VARCHAR(100) NOT NULL,
    STATUS_TYPE ENUM("RESERVERT","REFUND") DEFAULT "RESERVERT",
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
);
