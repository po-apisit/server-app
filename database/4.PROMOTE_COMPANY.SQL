CREATE TABLE IF NOT EXISTS TB_PROMOTE_FOR_COMPANY_POST(
    ID_PROMOTE_FOR_COMPANY INT AUTO_INCREMENT PRIMARY KEY,
    TITLE VARCHAR(100) NOT NULL,
    DESCRIPTION VARCHAR(255) NOT NULL,
    PERIOD_DATE INT ,
    PRICE DECIMAL(10,2)
    VISIBILITE_TYPE ENUM("PROVINCE","ALL") DEFAULT "PROVINCE",
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
);

CREATE TABLE IF NOT EXISTS TB_COMPANY_PROMOTE(
    ID_COMPANY_PROMOTE VARCHAR(50) PRIMARY KEY,
    ID_COMPANY VARCHAR(50),
    STATUS ENUM("PENDING","TODO","COMPLETE") DEFAULT "PENDING",
    ID_PROVINCE INT,
    ID_PROMOTE_FOR_COMPANY INT,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_COMPANY) REFERENCES TB_COMPANY (ID_COMPANY),
    FOREIGN KEY (ID_PROMOTE_FOR_COMPANY) REFERENCES TB_PROMOTE_FOR_COMPANY_POST (ID_PROMOTE_FOR_COMPANY),
    FOREIGN KEY (ID_PROVINCE) REFERENCES TB_PROVINCE (ID_PROVINCE)
)

CREATE TABLE IF NOT EXISTS TB_BILL_COMPANY_PROMOTE(
    ID_BILL_COMPANY_PROMOTE INT AUTO_INCREMENT PRIMARY KEY,
    BILL_NUMBER VARCHAR(50) NOT NULL,
    STATE ENUM ("PENDING","OVERDUE", "PAID") DEFAULT "PENDING",
    ID_COMPANY_PROMOTE VARCHAR(50),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_COMPANY_PROMOTE) REFERENCES TB_COMPANY_PROMOTE (ID_COMPANY_PROMOTE)
)

CREATE TABLE IF NOT EXISTS TB_COMPANY_CLOSE_VISIBILITY(
    ID_COMPANY_CLOSE_VISIBILITY INT AUTO_INCREMENT PRIMARY KEY,
    ID_BILL_COMPANY_PROMOTE INT,
    ID_COMPANY VARCHAR(50),
    STATE ENUM("CLOSE","OPEN") DEFAULT "CLOSE",
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_COMPANY) REFERENCES TB_COMPANY (ID_COMPANY)
)