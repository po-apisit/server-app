CREATE TABLE IF NOT EXISTS TB_PROMOTE_FOR_ASVERTISER(
    ID_PROMOTE_FOR_ASVERTISER INT AUTO_INCREMENT PRIMARY KEY,
    TITLE VARCHAR(100) NOT NULL,
    DESCRIPTION VARCHAR(255) NOT NULL,
    PERIOD_DATE INT ,
    PRICE DECIMAL(10,2),
    VISIBILITE_TYPE ENUM("PROVINCE","ALL") DEFAULT "PROVINCE",
    ID_PROVINCE INT,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_PROVINCE) REFERENCES TB_PROVINCE (ID_PROVINCE)
);

CREATE TABLE IF NOT EXISTS TB_ASVERTISER_PROMOTE(
    ID_ASVERTISER_PROMOTE VARCHAR(50) PRIMARY KEY,
    ID_USER VARCHAR(50),
    STATUS ENUM ("PENDING","TODO","COMPLETE") DEFAULT "PENDING",
    ID_PROVINCE INT,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_USER) REFERENCES TB_USER (ID_USER),
    FOREIGN KEY (ID_PROVINCE) REFERENCES TB_PROVINCE (ID_PROVINCE)
);

CREATE TABLE IF NOT EXISTS TB_BILL_ASVERTISER_PROMOTE(
    ID_BILL_ASVERTISER_PROMOTE INT AUTO_INCREMENT PRIMARY KEY,
    BILL_NUMBER VARCHAR(50) NOT NULL,
    STATE ENUM ("PENDING", "OVERDUE", "PAID") DEFAULT "PENDING",
    ID_ASVERTISER_PROMOTE VARCHAR(50),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_ASVERTISER_PROMOTE) REFERENCES TB_ASVERTISER_PROMOTE (ID_ASVERTISER_PROMOTE)
);

CREATE TABLE IF NOT EXISTS TB_ASVERTISER_CLOSE_VISIBILITY(
    ID_ASVERTISER_CLOSE_VISIBILITY INT AUTO_INCREMENT PRIMARY KEY,
    ID_BILL_ASVERTISER_PROMOTE INT,
    ID_USER VARCHAR(50),
    STATE ENUM("CLOSE","OPEN") DEFAULT "CLOSE",
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_USER) REFERENCES TB_USER (ID_USER)
);  



