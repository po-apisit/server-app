CREATE TABLE IF NOT EXISTS TB_PROMOTE_FOR_ASVERTISER(
    ID_PROMOTE_FOR_ASVERTISER INT AUTO_INCREMENT PRIMARY KEY,
    TITLE VARCHAR(100) NOT NULL,
    DESCRIPTION VARCHAR(255) NOT NULL,
    PERIOD_DATE INT ,
    PRICE DECIMAL(10,2)
    VISIBILITE_TYPE ENUM("PROVINCE","ALL") DEFAULT "PROVINCE",
    ID_PROVINCE INT,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    DELETED_AT TIMESTAMP DEFAULT NULL, -- เพิ่มคอลัมน์สำหรับเก็บวันที่ลบข้อมูล
    FOREIGN KEY (ID_PROVINCE) REFERENCES TB_PROVINCE (ID_PROVINCE)
);



